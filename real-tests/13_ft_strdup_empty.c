#include "tests.h"

int	test_strdup_empty(void)
{
	char	*dup;

	dup = ft_strdup("");
	if (dup && ft_strlen(dup) == 0)
	{
		free(dup);
		return (0);
	}
	if (dup)
		free(dup);
	return (-1);
}
